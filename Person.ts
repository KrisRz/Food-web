// Define a class named Person
class Person {
    // Properties with type annotations
    name: string;
    age: number;

    // Constructor to initialize the properties
    constructor(name: string, age: number) {
        this.name = name;
        this.age = age;
    }

    // Method to display person's details
    displayDetails(): void {
        console.log(`Name: ${this.name}, Age: ${this.age}`);
    }

    // Method to determine if the person is an adult
    isAdult(): boolean {
        return this.age >= 18;
    }
}

// Create instances of the Person class
let person1 = new Person("Alice", 25);
let person2 = new Person("Bob", 15);

// Display details and check if they are adults
person1.displayDetails(); // Output: Name: Alice, Age: 25
console.log(person1.isAdult()); // Output: true

person2.displayDetails(); // Output: Name: Bob, Age: 15
console.log(person2.isAdult()); // Output: false
